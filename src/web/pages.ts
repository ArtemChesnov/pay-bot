/**
 * Экранирует HTML-спецсимволы для безопасной вставки в разметку.
 * @param {string} s - Исходная строка
 * @returns {string} Экранированная строка
 */
function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

